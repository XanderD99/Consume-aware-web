			<h2>Objectives</h2>
			<p>
				The main objective of the project is to enhance the quality of
				innovative higher education on consumer awareness. It will be
				done through detailed objectives which include: creation and
				implementation of the innovation and interactive open
				educational module (MOOC), development of a model for academic
				innovative teaching and finally creation of an open space of
				integration and exchange of good practice between Universities
				from EU. Indirect objective concerns an increase of European
				consumers rights awareness. Project involves the implementation
				of e-learning platform which includes four issues: Consumer
				protection Organizational Structure
			</p>
