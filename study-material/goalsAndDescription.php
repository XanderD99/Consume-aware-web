            <h3>Goals and description</h3>

            <p>The mobile application is an innovative tool used within the learning process, and moreover will be a useful solution for all 
                consumers within the European Union.</p>

                    <p>The mobile application will be developed on the basis of database, which include all possible identified problems 
                        concerning consumers rights and their protection that they may face in their everyday life. The database will include 
                        a structured set of data concerning the possible consumer problems and solutions, taking into consideration consumer 
                        rights. The problems and solutions will be categorized according to country of origin. The identification of those 
                        possible problems as well as ways of solving them will be performed during the international workshops and meetings, 
                        and this will ensure that the identified issues will be relevant to consumers regardless their nation. The database 
                        will be updated on a regular basis with new issues, first of all by participating institutions in this project, and 
                        then by all institutions which will be interested in implementation of Consume-aware MOOC. Consumers who will download 
                        the app from app store will also have an opportunity to contact the consortium with problems they have (if the one will 
                        be not resolved inside the app – it will enable genuine up-to-date knowledge)
                    </p>
                    
                    <p>The mobile application will have an intuitive search engine, thanks to which it will be possible to find a particular 
                        consumer rights problem and possible solutions.
                    </p>
                    
                    <p>These innovative tools will have the following objectives:</p>
                    <ul class="list">
                        <li class="list-item">
                            learning objective: students participating in Consume-aware MOOC will use this tool as a training material – 
                            a real case studies – they will gain a possibility to learn about consumer rights-related issues in an interesting 
                            and interactive way, which will encourage them to explore the mobile application database,
                        </li>
                        <li class="list-item">
                                practical objective: consumers from European Union countries receive an easy to use mobile solution, which will 
                                support them in their everyday consumption experience and increase their awareness about their rights and related 
                                threats deriving from good and services providers. When such consumer face a consumer rights-related problem with 
                                purchased good or service, no matter in which country, thanks to this application he or she will be able to 
                                quickly find a solution and advices how to fight for his or her consumer rights.
                        </li>
                    </ul>
                    
                    <p>The creation of this application will enhance the awareness of consumers, also those who are students, about their rights and 
                    protection and will contribute to make their life easier. Moreover this will be created by international teams, and such 
                    cooperation will develop a cross-cultural dialogue and understanding. What is more using the mobile application will 
                    familiarize all stakeholders with innovative solutions and develop their ICT skills. </p>
