<h2>University of Trento </h2>
<p>
    The University of Trento (UniTrento) is a dynamic, middle-size University (with 16.548 students enrolled in 2014-2015, 586 professors/researchers and 704 administrative staff members), located in the North East of Italy. Founded in 1962, it has constantly pursued the improvement of the quality of research and teaching and the strengthening of its international dimension, networking with qualified universities and research centres from all over the world, making its campuses international and encouraging the presence in Trento of foreign visiting professors, researchers and students from all over the world. The strong commitment in international research and mobility projects have boosted its attractiveness and position in both national and international rankings.
</p>
<p>
    It is structured in 10 Departments (Economics and Management - which will be involved in the Consume Aware project - , Law, Sociology and Social Research, Humanities, Psychology and Cognitive Science, Industrial Engineering, Civil, Environmental and Mechanical Engineering, Information Engineering and Computer Science, Physics, Mathematics) and 3 Centres (International Studies, Integrative Biology and Mind/Brain Sciences) that promote, coordinate and manage the university teaching and research activities. The broad academic offer is complemented by a proven experience in the organisation of double, multiple and joint degrees, international and European master and
    PhD programmes, EIT ICT Labs, student and staff mobility, summer schools, workshops, joint
    projects, ect, shared with partners over the years also under different European schemes (e.g. LLP- Erasmus, Leonardo da Vinci, Erasmus Mundus, Erasmus+, Tempus, Jean Monnet, ALFA, Asialink, Bilateral Cooperation Programme EU-Canada, EU-Australia, EU-China, etc.).
</p>
<p>
    UniTrento offers 5 libraries, open even during the evening, with IT rooms equipped with PCs and a laptop PC services. UniTrento also operates its own Language Learning Centre with extensive and intensive courses in 7 foreign languages, as well as courses in Italian for foreigners.
</p>
