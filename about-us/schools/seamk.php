<h2>SeAMK</h2>
<p>
Seinäjoki University of Applied Sciences (SeAMK) is a multidisciplinary institution of HE with almost 5 000 students, established in1992 through a merger of various institutes each of which had a history of more than 100 years. SeAMK is a non-profit government dependent higher education institution (HEI) and a limited company in its organization form. The learning process and the research, development and innovation (RDI) process are the core processes. They are backed by the process of strategic management and leadership and by the support process. Internationality is integrated into all of these processes. The mission of SeAMK is to advance well-being in the region of South Ostrobothnia and at national level. The vision is to be an international institution of HE with an entrepreneurial spirit – best HEI for the student.The number of full-time students is 4800 and academic and other staff members 350. Seinäjoki University of Applied Sciences has 19 Bachelor and 8 Master degree programmes. Three degree programmes are totally taught in English (International Business, Nursing and a Master's Degree in International Business Management). Also double degree programmes are available in the fields of technology and business.
</p>
