<h2>University of Bucharest</h2>
<p>The University of Bucharest is a state institution of higher education, with legal personality, non- profit, public, and with a non-political status. The University of Bucharest was founded by the Decree no. 765 of July 4th 1864 of Prince Alexandru Ioan Cuza and is one of the most important higher education institutions in Romania. In the beginning, the Faculties of Law, Science and Letters were all together in one wing of the building. The University of Bucharest has several offices in Bucharest of the faculties and departments. At present, University of Bucharest is characterized by some statistical data: 19 faculties, more than 30 research centers, more than 32 000 students, 21 doctoral schools, hundreds of PhD-coordinators, about 1300 academic staff and 1100 administrative staff. In 2015 at an institutional level ICUB – Research Institute of the University of Bucharest was founded to encourage research of excellence in the 4 areas of domains that characterize the institutional development - Earth, Environmental and Life Sciences, Exact Sciences, Humanities and Social Sciences. ICUB also encourages international collaborations. In 2012 an international evaluation IEP – Institutional Evaluation Programme appreciated the strengths of the institution and the most recent national external evaluation ARACIS (Romanian Agency for Quality Assurance in Higher Education) conferred University of Bucharest the highest certification of recognition. At present University of Bucharest is considered a research intensive institution and is ranked in several international university rankings, such as ARWU, QS, Thomson Reuters, Times Higher Education.</p>
