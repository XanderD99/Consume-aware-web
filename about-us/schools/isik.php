<h2>IŞIK University</h2>
<p>IŞIK University, which was founded by the Feyziye Schools Foundation, an institution that is one of the oldest education foundations with a 130-year educational legacy, first opened its doors in 1996. The Feyziye Schools Foundation, an institution that was initially founded under the name of Feyz-i Sıbyan School in 1885 in Thessaloniki and witnessed the period from the Ottoman Empire to the Republic of Turkey, assumed the name “Işık Lisesi” with Mustafa Kemal Atatürk’s approval. “Işık Lisesi” carries the honor and dignity of uniting with Ataturk’s primary school Şemsi Efendi Mektebi. IŞIK University, along with the kindergartens, elementary and primary schools of Nişantaşı, Ayazağa, Erenköy and Ispartakule, has been added to the chain of education of the Feyziye Schools Foundation, and continues its educational mission with campuses on both the Asian and European sides of Istanbul – in Şile and Maslak respectively. With its universal standards in academic programs and its modern campuses, along with its academic staff that is fueled by the “Işık Tradition”, the University is preparing a bright future for its students. IŞIK University strives to prepare students with the qualifications needed to take on the challenges of the 21st Century, while equipping them with the values and ethics of our 130 year old parent foundation; Feyziye Schools Foundation and raise “good people.” It is a genuine campus university, with its facilities built on 62 acres of land in Şile and Maslak, providing students with accommodation, security and health services 24/7.</p>
