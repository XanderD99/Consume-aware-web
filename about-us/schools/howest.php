<h2>Howest</h2>
<p>Howest is a dynamic University of Applied Sciences known for its innovative
    and interdisciplinary approach to education and research, and its close
    collaboration with industry, business and the social profit sector.
    We offer 24 Bachelor and 10 Associate Degrees with a strong practical
    focus, in the areas of Business &amp; Management, Industrial Sciences &amp;
    Technology, Digital Design, Architecture, Healthcare, Education and Social
    Sciences.  Howest is a member of Ghent University Association, which
    provides high-quality higher education to about 1/3 of Flanders’ student
    population.
</p>
<p>
    Our four campuses are located in the historical cities of Bruges and Kortrijk,
    in the province of West Flanders, Belgium.
    Howest continuously adapts its curricula to include new areas of knowledge
    and to put the latest research into practice.  Creativity and entrepreneurship
    are part of the Howest DNA.  Field-driven assignments and on-the-job
    training are key components of each programme.  

</p>
<p>
    Over the years, Howest has built a solid reputation in application-oriented
